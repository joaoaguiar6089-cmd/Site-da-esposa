import { useState, useEffect } from "react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Textarea } from "@/components/ui/textarea";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from "@/components/ui/select";
import { ArrowLeft, Calendar } from "lucide-react";
import { supabase } from "@/integrations/supabase/client";
import { useToast } from "@/hooks/use-toast";
import type { Client } from "@/pages/Agendamento";

interface Procedure {
  id: string;
  name: string;
  description: string;
  duration: number;
  price: number;
}

interface AgendamentoFormProps {
  client: Client;
  onAppointmentCreated: () => void;
  onBack: () => void;
}

const AgendamentoForm = ({ client, onAppointmentCreated, onBack }: AgendamentoFormProps) => {
  const [procedures, setProcedures] = useState<Procedure[]>([]);
  const [formData, setFormData] = useState({
    procedure_id: "",
    appointment_date: "",
    appointment_time: "",
    notes: "",
  });
  const [loading, setLoading] = useState(false);
  const [loadingProcedures, setLoadingProcedures] = useState(true);
  const { toast } = useToast();

  const loadProcedures = async () => {
    try {
      const { data, error } = await supabase
        .from('procedures')
        .select('*')
        .order('name');

      if (error) throw error;

      setProcedures(data || []);
    } catch (error) {
      console.error('Erro ao carregar procedimentos:', error);
      toast({
        title: "Erro",
        description: "Erro ao carregar procedimentos.",
        variant: "destructive",
      });
    } finally {
      setLoadingProcedures(false);
    }
  };

  useEffect(() => {
    loadProcedures();
  }, []);

  const [availableTimes, setAvailableTimes] = useState<string[]>([]);

  const generateTimeOptions = () => {
    const times = [];
    for (let hour = 8; hour <= 18; hour++) {
      for (let minute = 0; minute < 60; minute += 30) {
        const timeString = `${hour.toString().padStart(2, '0')}:${minute.toString().padStart(2, '0')}`;
        times.push(timeString);
      }
    }
    return times;
  };

  const loadAvailableTimes = async (date: string) => {
    if (!date) {
      setAvailableTimes([]);
      return;
    }

    try {
      const { data, error } = await supabase
        .from('appointments')
        .select('appointment_time')
        .eq('appointment_date', date)
        .eq('status', 'agendado');

      if (error) throw error;

      const bookedTimes = data?.map(apt => apt.appointment_time) || [];
      const allTimes = generateTimeOptions();
      const available = allTimes.filter(time => !bookedTimes.includes(time));
      
      setAvailableTimes(available);
    } catch (error) {
      console.error('Erro ao carregar hor√°rios:', error);
      setAvailableTimes(generateTimeOptions());
    }
  };

  const getMinDate = () => {
    const tomorrow = new Date();
    tomorrow.setDate(tomorrow.getDate() + 1);
    return tomorrow.toISOString().split('T')[0];
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    
    if (!formData.procedure_id || !formData.appointment_date || !formData.appointment_time) {
      toast({
        title: "Campos obrigat√≥rios",
        description: "Por favor, preencha todos os campos obrigat√≥rios.",
        variant: "destructive",
      });
      return;
    }

    setLoading(true);
    
    try {
      const { error } = await supabase
        .from('appointments')
        .insert({
          client_id: client.id,
          procedure_id: formData.procedure_id,
          appointment_date: formData.appointment_date,
          appointment_time: formData.appointment_time,
          notes: formData.notes.trim() || null,
          status: 'agendado',
        });

      if (error) throw error;

      // Enviar mensagem WhatsApp de confirma√ß√£o
      try {
        const selectedProc = procedures.find(p => p.id === formData.procedure_id);
        const message = `ü©∫ *Agendamento Confirmado*\n\nOl√° ${client.nome}!\n\nSeu agendamento foi confirmado:\n\nüìÖ Data: ${new Date(formData.appointment_date).toLocaleDateString('pt-BR')}\n‚è∞ Hor√°rio: ${formData.appointment_time}\nüíâ Procedimento: ${selectedProc?.name}\nüí∞ Valor: R$ ${selectedProc?.price?.toFixed(2)}\n\nüìç Local: Tef√©-AM - Av. Brasil, 63b\n\nPara reagendamentos em Manaus, entre em contato via WhatsApp.\n\nObrigado pela confian√ßa! üôè`;
        
        await supabase.functions.invoke('send-whatsapp', {
          body: {
            to: client.celular,
            message: message
          }
        });
      } catch (whatsappError) {
        console.error('Erro ao enviar WhatsApp:', whatsappError);
        // N√£o falha o agendamento se o WhatsApp n√£o funcionar
      }

      toast({
        title: "Agendamento realizado!",
        description: "Seu agendamento foi criado com sucesso. Uma confirma√ß√£o ser√° enviada via WhatsApp.",
      });

      onAppointmentCreated();
    } catch (error) {
      console.error('Erro ao criar agendamento:', error);
      toast({
        title: "Erro",
        description: "Erro ao criar agendamento. Tente novamente.",
        variant: "destructive",
      });
    } finally {
      setLoading(false);
    }
  };

  const selectedProcedure = procedures.find(p => p.id === formData.procedure_id);

  if (loadingProcedures) {
    return (
      <Card className="w-full">
        <CardContent className="p-6">
          <div className="text-center">Carregando procedimentos...</div>
        </CardContent>
      </Card>
    );
  }

  return (
    <Card className="w-full">
      <CardHeader className="text-center">
        <div className="flex items-center justify-center gap-2 mb-2">
          <Calendar className="w-6 h-6 text-primary" />
          <CardTitle className="text-xl">Novo Agendamento</CardTitle>
        </div>
        <p className="text-muted-foreground">
          {client.nome} {client.sobrenome}
        </p>
      </CardHeader>
      <CardContent>
        <form onSubmit={handleSubmit} className="space-y-4">
          <div>
            <label htmlFor="procedure" className="text-sm font-medium">
              Procedimento *
            </label>
            <Select
              value={formData.procedure_id}
              onValueChange={(value) => setFormData(prev => ({ ...prev, procedure_id: value }))}
            >
              <SelectTrigger className="mt-1">
                <SelectValue placeholder="Selecione um procedimento" />
              </SelectTrigger>
              <SelectContent>
                {procedures.map((procedure) => (
                  <SelectItem key={procedure.id} value={procedure.id}>
                    <div>
                      <div className="font-medium">{procedure.name}</div>
                      <div className="text-sm text-muted-foreground">
                        {procedure.duration}min - R$ {procedure.price?.toFixed(2)}
                      </div>
                    </div>
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
            {selectedProcedure && (
              <p className="text-sm text-muted-foreground mt-1">
                {selectedProcedure.description}
              </p>
            )}
          </div>

          <div>
            <label htmlFor="date" className="text-sm font-medium">
              Data *
            </label>
            <Input
              id="date"
              type="date"
              min={getMinDate()}
              value={formData.appointment_date}
              onChange={(e) => {
                const newDate = e.target.value;
                setFormData(prev => ({ ...prev, appointment_date: newDate, appointment_time: "" }));
                loadAvailableTimes(newDate);
              }}
              className="mt-1"
              required
            />
          </div>

          <div>
            <label htmlFor="time" className="text-sm font-medium">
              Hor√°rio *
            </label>
            <Select
              value={formData.appointment_time}
              onValueChange={(value) => setFormData(prev => ({ ...prev, appointment_time: value }))}
            >
              <SelectTrigger className="mt-1">
                <SelectValue placeholder="Selecione um hor√°rio" />
              </SelectTrigger>
              <SelectContent>
                {availableTimes.length > 0 ? (
                  availableTimes.map((time) => (
                    <SelectItem key={time} value={time}>
                      {time}
                    </SelectItem>
                  ))
                ) : (
                  <SelectItem value="no-times" disabled>
                    {formData.appointment_date ? "Nenhum hor√°rio dispon√≠vel" : "Selecione uma data primeiro"}
                  </SelectItem>
                )}
              </SelectContent>
            </Select>
          </div>

          <div>
            <label htmlFor="notes" className="text-sm font-medium">
              Observa√ß√µes
            </label>
            <Textarea
              id="notes"
              placeholder="Alguma observa√ß√£o especial..."
              value={formData.notes}
              onChange={(e) => setFormData(prev => ({ ...prev, notes: e.target.value }))}
              className="mt-1"
              rows={3}
            />
          </div>
          
          <div className="flex gap-2 pt-2">
            <Button
              type="button"
              variant="outline"
              onClick={onBack}
              className="flex items-center gap-2"
            >
              <ArrowLeft className="w-4 h-4" />
              Voltar
            </Button>
            <Button
              type="submit"
              disabled={loading}
              className="flex-1"
            >
              {loading ? "Agendando..." : "Confirmar Agendamento"}
            </Button>
          </div>
        </form>
      </CardContent>
    </Card>
  );
};

export default AgendamentoForm;